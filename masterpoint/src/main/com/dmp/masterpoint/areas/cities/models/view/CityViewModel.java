package com.dmp.masterpoint.areas.cities.models.view;

public class CityViewModel {
    private String id;

    private String name;

    public CityViewModel() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
